"""
2. Crie um programa que leia um número natural n dado pelo
usuário e exiba só os n primeiros pares a partir de 0. Por
exemplo, se n=6 ser exibido 0 2 4 6 8 10

"""

final = int(input('Digite um número: '))

inicio = 0 # variável de controle
soma = 0

while inicio < final :

    print(soma)
    soma = soma + 2

    inicio = inicio + 1  